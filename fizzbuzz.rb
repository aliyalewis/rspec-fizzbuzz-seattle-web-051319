def fizzbuzz(x)
  if x % == fizzbuzz(3)
    puts "Fizz"
  elsif x % == fizzbuzz(5)
    puts "Buzz"
  elsif x % == fizzbuzz(15)
    puts "FizzBuzz"
  else x % == fizzbuzz(4)
    puts nil
end

